    private void refreshTable() {
        DefaultTableModel model = (DefaultTableModel) tblExpressManager.getModel();
        model.setRowCount(0);

        for(ExpressManager em: network.getExpressManagerDir().getExpressManagerList()){
            Object row[] = new Object[5];
             row[0] = em;
           row[1] = em.getName();
           row[2] = em.getAddress();
           row[3] = em.getCompany();
           row[4] = em.getPhone();

            
            model.addRow(row);
       
    }
        lbllocation.setText( network.getName());
    }
