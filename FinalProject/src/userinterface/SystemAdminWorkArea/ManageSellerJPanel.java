    private void refreshTable() {
      DefaultTableModel model = (DefaultTableModel) tblSeller.getModel();
        model.setRowCount(0);
     
       for(Seller s: network.getSellerDirectory().getSellerList()){
            Object row[] = new Object[5];
           row[0] = s;
           row[1] = s.getName();
           row[2] = s.getAddress();
           row[3] = s.getPhone();
           row[4] = s.getType();
           
            model.addRow(row);       
    }
       lbllocation.setText( network.getName());
    }
