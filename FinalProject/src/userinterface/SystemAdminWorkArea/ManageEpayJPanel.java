    private void btnCreateCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateCustomerActionPerformed
        // TODO add your handling code here:
           if(txtEpayerName.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Invalid Name!");
        }else if(txtUserName.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Invalid UserName!");
        }else if(txtPassword.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Invalid Password!");
        }else{
        String userName = txtUserName.getText();
        String password = txtPassword.getText();
        String EpayerName = txtEpayerName.getText();
        String phone = txtPhone.getText();
        
        
        UserAccount useraccount=new UserAccount();
        Employee e1 = system.getEmployeeDirectory().createEmployee(EpayerName);
        useraccount=system.getUserAccountDirectory().createUserAccount(userName, password, e1, new EpayerRole());
        network.getEpayerdir().createEpayer(EpayerName,phone,useraccount);
        
        
        JOptionPane.showMessageDialog(null, "User Account created successfully.");
       
        
        txtEpayerName.setText("");
        txtUserName.setText("");
        txtPassword.setText("");
        txtPhone.setText("");
        
        refreshTable();
    }//GEN-LAST:event_btnCreateCustomerActionPerformed
   }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         int selectedRow = tblCustomers.getSelectedRow();
        
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a row!", "Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
        
      Epayer ep = (Epayer)tblCustomers.getValueAt(selectedRow, 0);
      network.getEpayerdir().getEpayerList().remove(ep);
      UserAccount ua=new UserAccount();
      ua=ep.getUserAccount();
      system.getUserAccountDirectory().getUserAccountList().remove(ua);
       
          refreshTable();
         JOptionPane.showMessageDialog(null, "Delete the supplier successfully!");
    }//GEN-LAST:event_jButton1ActionPerformed

    private void btnModifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifyActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblCustomers.getSelectedRow();
        
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a row!", "Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
      Epayer ep = (Epayer)tblCustomers.getValueAt(selectedRow, 0);
      txtEpayerName.setText(ep.getName());
      txtUserName.setText(ep.getUserAccount().getUsername());
      txtPassword.setText(ep.getUserAccount().getPassword());
      txtPhone.setText(ep.getPhone());
      
      
      
      
       btnSave.setEnabled(true);
       btnModify.setEnabled(false);
    }//GEN-LAST:event_btnModifyActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
          int selectedRow = tblCustomers.getSelectedRow();
        
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a row!", "Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
      Epayer ep = ( Epayer)tblCustomers.getValueAt(selectedRow, 0);
      
     
      
       if(system.getUserAccountDirectory().checkIfUsernameIsUnique(txtUserName.getText())){}
            else{
                JOptionPane.showMessageDialog(null, "This username has existed!", "Warning",JOptionPane.WARNING_MESSAGE);
                return;
            }
        
        
        
   
        
      
      ep.setName( txtEpayerName.getText());
      ep.setPhone(txtPhone.getText());
      ep.getUserAccount().setPassword(txtPassword.getText());
      ep.getUserAccount().setUsername(txtUserName.getText());
     
       
      
        txtEpayerName.setText("");
        txtUserName.setText("");
        txtPassword.setText("");
        txtPhone.setText("");
        
        btnSave.setEnabled(false);
        btnModify.setEnabled(true);
        
        refreshTable();
    }//GEN-LAST:event_btnSaveActionPerformed


